% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
e = get(handles.edit1,'String');
h = get(handles.edit2,'String');
f = handles.tum(:,1);
g = handles.tum(1,:);
satir = length(g);
sutun = length(f);
degisken=0
for i=1:1:sutun
    if strcmp(handles.tum{i,1},e) ~= 0
        if strcmp(handles.tum{i,2},h) ~= 0
            set(handles.edit3,'String',handles.tum{i,1})
            set(handles.edit4,'String',handles.tum{i,2})
            set(handles.edit5,'String',handles.tum{i,3})
            set(handles.edit6,'String',handles.tum{i,4})
            set(handles.edit7,'String',handles.tum{i,5})
            set(handles.edit8,'String',handles.tum{i,6})
            set(handles.edit9,'String',handles.tum{i,7})
            degisken=1
        end
    end
end
set(handles.uibuttongroup1,'Visible','off')
if degisken == 0
    set(handles.text12,'String','HATA!!!')
    pause(2)
    set(handles.text12,'String','')
elseif degisken == 1
    set(handles.text12,'String','ÝÞLEM BAÞARILI!')
    set(handles.uipanel2,'Visible','On')
    pause(2)
    set(handles.text12,'String','')
end

